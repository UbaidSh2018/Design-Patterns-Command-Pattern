public class Stereo {

    public void on(){
        System.out.println("Stereo is On");
    }
    public void off(){
        System.out.println("Stereo is Off");
    }

    public void setCD(){
        System.out.println("Stereo CD is set");
    }

    public void setVolume(int vol){
        System.out.println("Stereo volume set to " + vol);
    }
}
